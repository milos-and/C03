char	*ft_strcat(char *dest, char *src)
{
	unsigned int i;
	unsigned int p;

	i = -1;
	p = -1;
	while (dest[++i] != '\0')
		;
	while (src[++p] != '\0')
		dest[i + p] = src[p];
	dest[i + p] = '\0';
	return (dest);
}
